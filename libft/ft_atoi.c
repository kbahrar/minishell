#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	nb;

	i = 0;
	nb = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] != '-')
	{
		if (str[i] == '+')
			i++;
		while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
			nb = nb * 10 + (str[i++] - '0');
	}
	else
	{
		i++;
		while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
		{
			nb = nb * 10 - (str[i++] - '0');
		}
	}
	return (nb);
}
